							<h3>Gestión Integral de Riesgos</h3>
							<p>
								Es el proceso estructurado, consistente y continuo para identificar, medir, monitorear, controlar, mitigar y divulgar todos los riesgos a los cuales la entidad supervisada se encuentra expuesta, en el marco del conjunto de estrategias, objetivos, políticas, procedimientos y acciones, establecidas por la entidad para este propósito.							
							</p>

							<h3>Unidad de Gestión de Riesgos</h3>
							<p>
								Órgano autónomo responsable de identificar, medir, monitorear, controlar, mitigar y divulgar todos los riesgos (crediticio, de mercado, liquidez, operativo, legal y otros) que enfrenta la entidad.
							</p>							

							<h3>Riesgo</h3>
							<p>
								Es la contingencia, probabilidad o posibilidad de que eventos, anticipados o no puedan tener un impacto adverso contra ingresos y/o patrimonio de la entidad.
							</p>

							<h3>Riesgo de Crédito</h3>
							<p>
								Probabilidad de que un deudor incumpla, en cualquier grado, con el repago de sus obligaciones con la Cooperativa, de modo tal que se genere una disminución en el valor presente del contrato.
							</p>

							<h3>Riesgo de Liquidez</h3>
							<p>
								Es la contingencia de que una entidad incurra en pérdida por la venta anticipada o forzosa de activos a descuentos inusuales y/o significativos con el propósito de contar rápidamente con los recursos necesarios para cumplir con sus obligaciones o por la imposibilidad de renovar o de contratar nuevos financiamientos en condiciones normales para la entidad supervisada.
							</p>

								<h3>Riesgo de Mercado</h3>
							<p>
								Es la probabilidad de que una entidad incurra en pérdidas por variaciones adversas en los factores de mercado, como tasas de interés, tipos de cambio y precios de activos subyacentes en operaciones financieras.
							</p>

									<h3>Riesgo Operativo</h3>
							<p>
								Es la posibilidad o probabilidad de que una entidad incurra en pérdidas por fraude interno o externo, fallas en las personas, procesos y sistemas, eventos internos de orden estratégico y operativo y otros eventos externos.
							</p>

